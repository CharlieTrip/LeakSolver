use crate::leakfun::LeakFun;

#[derive(Copy, Clone)]
pub struct Hamming8 {}

/// # 8-bit Hamming Weight Leakage Model
/// *AAA:* Strict usage of u8!

impl LeakFun<u8, u8> for Hamming8 {
  fn leak_f(input: u8) -> u8 {
    input.count_ones() as u8
  }

  fn inv_leak_f(weight: u8) -> Vec<u8> {
    match weight {
      0 => W0.to_vec(),
      1 => W1.to_vec(),
      2 => W2.to_vec(),
      3 => W3.to_vec(),
      4 => W4.to_vec(),
      5 => W5.to_vec(),
      6 => W6.to_vec(),
      7 => W7.to_vec(),
      8 => W8.to_vec(),
      _ => [].to_vec(),
    }
  }

  fn get_leak_f(&self) -> Box<(dyn Fn(&u8) -> u8)> {
    Box::new(|x| Hamming8::leak_f(*x))
  }

  fn get_inv_leak_f(&self) -> Box<dyn Fn(&u8) -> Vec<u8>> {
    Box::new(|x| Hamming8::inv_leak_f(*x))
  }
}

